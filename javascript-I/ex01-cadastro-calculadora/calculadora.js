// Calculadora de 4 operacoes

let num1 = parseFloat(prompt("Primeiro numero: "))
let num2 = parseFloat(prompt("Segundo numero: "))

window.alert(`
    Recruta cadastrado com Sucesso!

    soma =   ${num1} + ${num2} = ${num1 + num2}    
    subtracao =   ${num1} + ${num2} = ${num1 - num2}    
    multiplicacao =   ${num1} + ${num2} = ${num1 * num2}    
    divisao =   ${num1} + ${num2} = ${num1 / num2}    
`)